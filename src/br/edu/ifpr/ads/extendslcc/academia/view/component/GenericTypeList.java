    private DefaultListModel<T> listModel = new DefaultListModel<>();

    private void onInit(){

        updateItens();
        elementList.setModel( listModel );
        elementList.setSelectionMode( ListSelectionModel.SINGLE_SELECTION );

        elementList.getSelectionModel().addListSelectionListener( new ListSelectionListener(){

            @Override
            public void valueChanged( ListSelectionEvent arg0 ){

                if( elementList.getSelectedIndex() == -1 ){

                    btnUpdate.setEnabled( false );
                    btnDelete.setEnabled( false );

                }else{

                    btnUpdate.setEnabled( true );
                    btnDelete.setEnabled( true );

                }
            }

        } );
        
    }

    private void updateItens(){

        listModel.removeAllElements();

        D dao = generateDao();
        List<T> listTypes = dao.findAll();
        listTypes.forEach( type -> listModel.addElement( type ) );
        
    }

    public T getSelectedType(){

        return elementList.getSelectedValue();

    }

