    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if( elementList.getSelectedValue() != null ){

            String nome = JOptionPane.showInputDialog( this, "Insira o novo nome de " + title + " de " + elementList.getSelectedValue().getNome(), elementList.getSelectedValue().getNome() );

            if( nome == null ){
                return;
            }

            D dao = generateDao();
            T type = elementList.getSelectedValue();
            System.out.println( type );
            System.out.println( "id: " + type.getId() );
            type.setNome( nome );
            dao.update( type );

            int lastIndex = type.getId();
            JOptionPane.showMessageDialog( this, title + " \"" + nome + "\" alterado com sucesso!" );

        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed

        String nome = JOptionPane.showInputDialog( this, "Cadastrar " + title );

        if( nome == null ){
            return;
        }

        D dao = generateDao();
        T type = generatetype();
        type.setNome( nome );
        dao.create( type );

        JOptionPane.showMessageDialog( this, title + " \"" + nome + "\" cadastrado com sucesso!" );
        listModel.addElement( type );
        elementList.setSelectedValue( type, true );

    }//GEN-LAST:event_btnCadActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        if( JOptionPane.showConfirmDialog( this, "Confirmar exclusão de " + title + ": "
                + elementList.getSelectedValue().getNome(), "", JOptionPane.YES_NO_OPTION ) == JOptionPane.YES_OPTION ){

            D dao = generateDao();
            T type = elementList.getSelectedValue();
            
            if( dao.delete( type ) ){
                
                JOptionPane.showMessageDialog( this, title + " \"" + type.getNome() + "\" excluido com sucesso!" );
                
            }else{
                
                JOptionPane.showMessageDialog( this, title + " \"" +  type.getNome() + "\" não pode ser deletado pois é referenciado por outro Item." );
                
            }

        }

    }//GEN-LAST:event_btnDeleteActionPerformed


