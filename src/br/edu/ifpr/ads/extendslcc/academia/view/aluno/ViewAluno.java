    private GenericComboModel<Turma> comboModelTurma;
    private FilterListModel<Aluno> listElemetsModel;
    private Aluno currentAluno;

    private void onInit(){

        enableComponents( false );
        Connection con;

        try{

            con = ConnectionFactory.createConnectionToMySQL();
            AlunoDao alunoDao = new AlunoDao( con );
            listElemetsModel = new FilterListModel<Aluno>( alunoDao.findAll() );
            listElements.setModel( listElemetsModel );
            con.close();

        }catch( SQLException ex ){

            System.out.println( "Erro na conexão do list Aluno " + ex.getMessage() );

        }

        comboModelTurma = new GenericComboModel<>();
        cbTurma.setModel( comboModelTurma );

        updateComboTurma();

        tfSearch.getDocument().addDocumentListener( new DocumentListener(){
            @Override
            public void insertUpdate( DocumentEvent e ){
                filter();
            }

            @Override
            public void removeUpdate( DocumentEvent e ){
                filter();
            }

            @Override
            public void changedUpdate( DocumentEvent e ){
                filter();
            }

            private void filter(){

                listElemetsModel.filter( tfSearch.getText() );

            }

        } );

        listElements.getSelectionModel().addListSelectionListener( new ListSelectionListener(){//seleção alterada

            @Override
            public void valueChanged( ListSelectionEvent e ){

                if( !e.getValueIsAdjusting() ){

                    if( listElements.getSelectedIndex() >= 0 ){

                        ViewAluno.this.currentAluno = listElements.getSelectedValue();
                        fillFieldsByAluno( ViewAluno.this.currentAluno );
                        btnUpdate.setEnabled( true );
                        btnDelete.setEnabled( true );

                    }else{

                        enableComponents( false );
                        btnUpdate.setEnabled( false );
                        btnDelete.setEnabled( false );

                    }

                }

            }

        } );

    }

