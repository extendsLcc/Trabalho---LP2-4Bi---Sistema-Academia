    private void updateComboTurma(){

        try{

            Connection con = ConnectionFactory.createConnectionToMySQL();

            TurmaDao turmaDao = new TurmaDao( con );
            List<Turma> turmas = turmaDao.findAll();
            comboModelTurma.clear();
            comboModelTurma.addElement( null );
            comboModelTurma.addListElements( turmas );
            con.close();
            System.out.println( "turmas: " + turmas );

        }catch( SQLException ex ){

            System.out.println( "Erro na conexão do list Aluno > ComboBox Turma" + ex.getMessage() );

        }

    }

    private void fillFieldsByAluno( Aluno aluno ){

        cbTurma.setSelectedItem( aluno.getTurma() );

        dateNascimento.getCurrent().setTime( aluno.getNascimento() );
        dateMatricula.getCurrent().setTime( aluno.getDataMatricula() );

        tfNome.setText( aluno.getNome() );
        tfTelefone.setText( aluno.getTelefone() );
        tfEndereco.setText( aluno.getEndereco() );
        tfAltura.setText( aluno.getAltura() + "" );
        tfPeso.setText( aluno.getPeso() + "" );

        if( aluno.getTurma() != null ){
            
            Turma turma = comboModelTurma.getElements().stream()
                    .filter( el -> el != null )
                    .filter( turm -> turm.getIdTurma() == aluno.getTurma().getIdTurma() )
                    .findFirst()
                    .get();

            comboModelTurma.setSelectedItem( turma );
            
        }else{
        
            cbTurma.setSelectedIndex( 0 );
            
        }

    }

    private void setAlunoByForms( Aluno aluno ){

        aluno.setNome( tfNome.getText() );
        aluno.setTelefone( tfTelefone.getText() );
        aluno.setEndereco( tfEndereco.getText() );
        aluno.setAltura( Float.parseFloat( tfAltura.getText() ) );
        aluno.setPeso( Float.parseFloat( tfPeso.getText() ) );

        aluno.setNascimento( dateNascimento.getCurrent().getTime() );
        aluno.setDataMatricula( dateMatricula.getCurrent().getTime() );

        aluno.setTurma( comboModelTurma.getSelectedItem() );

        if( aluno.getIdAluno() != -1 ){

            alunoDaoOperate( ( dao ) -> dao.update( aluno ) );

        }else{

            alunoDaoOperate( ( dao ) -> dao.create( aluno ) );

        }

    }

    private boolean alunoDaoOperate( Function< AlunoDao, Boolean> operation ){

        try{

            Connection con = ConnectionFactory.createConnectionToMySQL();

            AlunoDao alunoDao = new AlunoDao( con );
            return operation.apply( alunoDao );

        }catch( SQLException ex ){

            Logger.getLogger( ViewAluno.class.getName() ).log( Level.SEVERE, null, ex );

        }

        return false;

    }

